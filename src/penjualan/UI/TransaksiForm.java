    private void initTablePemesanan() {
        String[] bookColumns = new String[]{"ID Pemesanan", "ID Kue","Nama", "Total Harga", "Tanggal", "ID Konsumen"};
        //total lebar 650
        int[] columnWidth = {
            120, 80, 200, 100, 250, 120
        };

        Modeltable = new DefaultTableModel(bookColumns, 0);
        transaksiTabel.setModel(Modeltable);
        transaksiTabel.setRowHeight(20);

        int i = 0;
        for (int width : columnWidth) {
            TableColumn column = transaksiTabel.getColumnModel().getColumn(i++);
            column.setMaxWidth(width);
            column.setMaxWidth(width);
            column.setPreferredWidth(width);
        }
    }

    public static void loadPemesanan() {
        PemesananList = new ArrayList<>();
        PemesananList = PemesananManajer.showAllPemesanan();
        Modeltable.setRowCount(0);
        PemesananList.forEach(pemesanan -> {
            Modeltable.addRow(new Object[]{
                pemesanan.getId_pemesanan(),
                pemesanan.getId_kue(),
                pemesanan.getNama_konsumen(),
                pemesanan.getTotal_harga(),
                pemesanan.getTanggal_pemesanan(),
                pemesanan.getId_konsumen(),});
        });
    }

