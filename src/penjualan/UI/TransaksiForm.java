    private void btdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = transaksiTabel.getSelectedRow();
        if (selectedRow == -1) {
            showMessage("Pilih Baris", 2);
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus baris ini?", "Hapus Data",
                    JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.YES_OPTION) {
                try {
                    java.sql.Connection conn = ConnnectionHelper.getConnection();
                    Statement stmn = conn.createStatement();
                    pemesanan pmsn = PemesananList.get(selectedRow);
                    stmn.executeUpdate("DELETE FROM pemesanan WHERE id_pemesanan = " + pmsn.getId_pemesanan());
                    loadPemesanan();
                } catch (Exception e) {
                    System.out.println("Error:" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btdDeleteActionPerformed

