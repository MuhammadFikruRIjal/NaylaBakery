    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn = ConnnectionHelper.getConnection();
            Statement stmn = conn.createStatement();

            String query1 = "INSERT INTO pemesanan (`id_konsumen`,`id_kue`, `Nama_konsumen`,`Total_harga`, `Tanggal_pemesanan`) "
                    + "VALUES ('" + idKonsumen.getText() + "', '" + jtfIdKue.getText() + "', "
                    + "'" + namaKonsumen.getText() + "', '" + jtfAllTotal.getText() + "', '" + jdtTanggal.getDate() + "')";

            String query2 = "INSERT INTO pembayaran (`Total_bayar`, `Tanggal_pembayaran`) "
                    + "VALUES ('" + txtBayar.getText() + "', '" + jdtTanggal.getDate() + "')";

            stmn.executeUpdate(query1);
            stmn.executeUpdate(query2);

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            txtBayar.setText("");

        } catch (Exception e) {
            System.out.println("Error" + e.getMessage());
        }

    }//GEN-LAST:event_btnBayarActionPerformed

    private void btnProsesTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesTotalActionPerformed
        // TODO add your handling code here:
        double hargabarang, total;
        int jumlahbeli, stock;

        hargabarang = Double.parseDouble(jtfHarga.getText());
        jumlahbeli = Integer.parseInt(jtfPesan.getText());
        stock = Integer.parseInt(jtfStock.getText());

        if (stock < jumlahbeli) {
            JOptionPane.showMessageDialog(this, "Stok tidak mencukupi", "Maaf", JOptionPane.INFORMATION_MESSAGE);
            jtfPesan.setText("");
        } else {
            total = hargabarang * jumlahbeli;
            jtfTotal.setText(" " + total);
        }


    }//GEN-LAST:event_btnProsesTotalActionPerformed

