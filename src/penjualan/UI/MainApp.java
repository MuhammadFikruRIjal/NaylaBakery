    public void tampil_kue() {
        try {
            String query = "SELECT Nama_kue FROM produk order by Nama_kue asc";
            Connection conn = ConnnectionHelper.getConnection();
            Statement stm = conn.createStatement();

            ResultSet rs = stm.executeQuery(query);

            while (rs.next()) {
                Object[] ob = new Object[3];
                ob[0] = rs.getString(1);

                cmbPilihKue.addItem((String) ob[0]);

            }
            rs.close();
            stm.close();
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();

        } catch (Exception e) {
        }
    }

    public void tampil() {
        try {
            String query = "SELECT id_kue, Harga_kue, Stock_kue FROM produk where Nama_kue='" + cmbPilihKue.getSelectedItem() + "'";
            Connection conn = ConnnectionHelper.getConnection();
            Statement stm = conn.createStatement();

            ResultSet rs = stm.executeQuery(query);

            while (rs.next()) {
                Object[] ob = new Object[3];
                ob[0] = rs.getString(1);
                ob[1] = rs.getString(2);
                ob[2] = rs.getString(3);

                jtfIdKue.setText((String) ob[0]);
                jtfHarga.setText((String) ob[1]);
                jtfStock.setText((String) ob[2]);

            }
            rs.close();
            stm.close();
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();

        } catch (Exception e) {
        }
    }

