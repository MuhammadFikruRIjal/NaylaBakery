    private void sum() {
        DefaultTableModel paymentModel = (DefaultTableModel) paymentTable.getModel();
        int jumlah = paymentModel.getRowCount();
        double Total = 0;

        for (int i = 0; i < jumlah; i++) {
            Double total = Double.valueOf(paymentModel.getValueAt(i, 3).toString());
            Total += total;
        }
        jtfAllTotal.setText(Double.toString(Total));
    }


