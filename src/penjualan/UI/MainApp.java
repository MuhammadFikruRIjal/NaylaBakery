    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:

        try {
            paymentTable.setValueAt(idKonsumen.getText(), x, 0);
            paymentTable.setValueAt(jtfIdKue.getText(), x, 1);
            paymentTable.setValueAt(namaKonsumen.getText(), x, 2);
            paymentTable.setValueAt(jtfTotal.getText(), x, 3);
            paymentTable.setValueAt(jdtTanggal.getDate(), x, 4);
            x = x + 1;

            if (jtfPesan.getText().equals("")) {
                JOptionPane.showMessageDialog(null, " Tidak Boleh Kosong");
            } else if (jtfTotal.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");
            } else if (jdtTanggal.getDate().equals("")) {
                JOptionPane.showMessageDialog(null, "Tidak Boleh Kosong");

            } else {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }

            jtfPesan.setText("");
            jtfTotal.setText("");
            sum();

        } catch (Exception e) {
            System.out.println("Error" + e.getMessage());
        }

    }//GEN-LAST:event_btnTambahActionPerformed

